/*Example 6 Regarding Aritmetic Operator*/

#include <iostream>
using namespace std;

int main(){
    int a=5, b=3;
    cout<<(a+b)<<"\n";
    cout<<(a-b)<<"\n";
    cout<<(a*b)<<"\n";
    cout<<(a/b)<<"\n";
    cout<<(a%b)<<"\n"; //Modulus hold the remain
    return 0;
}

 /*
 ### Output ###
 8
 2
 15
 1
 2
 */